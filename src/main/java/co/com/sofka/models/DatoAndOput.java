package co.com.sofka.models;

public class DatoAndOput {
    private int firstData;
    private int outcome;
    private String wrongData;
    private String wrongOutcome;

    public int getFirstData() {
        return firstData;
    }

    public void setFirstData(int firstData) {
        this.firstData = firstData;
    }

    public int getOutcome() {
        return outcome;
    }

    public void setOutcome(int outcome) {
        this.outcome = outcome;
    }

    public String getWrongData() {
        return wrongData;
    }

    public void setWrongData(String wrongData) {
        this.wrongData = wrongData;
    }

    public String getWrongOutcome() {
        return wrongOutcome;
    }

    public void setWrongOutcome(String wrongOutcome) {
        this.wrongOutcome = wrongOutcome;
    }
}
